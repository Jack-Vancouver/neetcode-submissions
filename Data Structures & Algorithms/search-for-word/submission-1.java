class Solution {
    public boolean exist(char[][] board, String word) {
        int rows = board.length;
        int cols = board[0].length;
        
        // 1. 遍历棋盘的每一个格子，把它当作寻宝的起点
        for (int r = 0; r < rows; r++) {
            for (int c = 0; c < cols; c++) {
                // 只要有任何一个起点能找到完整的 word，就直接成功下班！
                if (dfs(board, word, r, c, 0)) {
                    return true;
                }
            }
        }
        return false;
    }

    // 核心质检员：当前在 (r, c) 位置，正在寻找 word 的第 index 个字符
    private boolean dfs(char[][] board, String word, int r, int c, int index) {
        // Base Case 1：全都找完了！(index 越界说明所有字母都匹配成功了)
        if (index == word.length()) {
            return true;
        }

        // Base Case 2：撞墙了，或者找错人了
        // 1. 越界了
        // 2. 当前格子的字母，和我们要找的字母不一样
        // 3. 踩到了 '#'，说明这是当前路径刚走过的死路
        if (r < 0 || c < 0 || r >= board.length || c >= board[0].length 
            || board[r][c] != word.charAt(index)) {
            return false;
        }

        // --- 下面是回溯算法的标准模板 ---

        // 1. 记录原来的真实字母
        char temp = board[r][c];
        
        // 2. 做选择（隐身术）：标记为已走过，防止自己人踩自己人
        board[r][c] = '#';

        // 3. 递归：向上下左右四个方向疯狂试探（只要有一条路通了就行！）
        boolean found = dfs(board, word, r + 1, c, index + 1) || // 往下走
                        dfs(board, word, r - 1, c, index + 1) || // 往上走
                        dfs(board, word, r, c + 1, index + 1) || // 往右走
                        dfs(board, word, r, c - 1, index + 1);   // 往左走

        // 4. 撤销选择（时光倒流）：不管刚刚那四条路通没通，我都要把现场恢复原样！
        // 因为如果当前路径失败了，其他从别的起点过来的路径可能还需要用这个字母。
        board[r][c] = temp;

        // 返回试探的结果
        return found;
    }
}

// DFS 回溯一步步推演
// 为了看清代码是怎么走的，我们设定一个极简的场景。
// 目标单词 (word)："ABC"
// 小棋盘 (board)：
//   0  1
// 0 A  B
// 1 D  C

// 主函数 exist 里的两个 for 循环会依次把棋盘上的每个格子当作起点。我们直接看当 r = 0, c = 0（即起点的字母是 'A'）时，dfs 函数是怎么运转的。
// 📍 第 1 层：起点 (0, 0)，字母 'A'，寻找 index = 0 ('A')
// 	1.	核对身份：当前字母是 'A'，我们要找的 word.charAt(0) 也是 'A'。匹配成功！没有撞墙。
// 	2.	保存并隐身：
// •	temp = 'A'（记住自己本来的样子）。
// •	board[0][0] = '#'（隐身，防止走回头路踩到自己）。
// •	此时棋盘变成：
//   0  1
// 0 #  B
// 1 D  C
// 	3.	四面出击（开始 || 连招）：寻找 index = 1（即字母 'B'）。
// •	往下走 (r+1)：到了 (1, 0) 字母是 'D'。'D' != 'B'，返回 false。
// •	往上走 (r-1)：到了 (-1, 0) 越界了，返回 false。
// •	往右走 (c+1)：到了 (0, 1) 字母是 'B'。进入下一层！
// 📍 第 2 层：位置 (0, 1)，字母 'B'，寻找 index = 1 ('B')
// 	1.	核对身份：当前是 'B'，我们要找的 word.charAt(1) 也是 'B'。匹配成功！
// 	2.	保存并隐身：
// •	temp = 'B'。
// •	board[0][1] = '#'。
// •	此时棋盘变成：
//   0  1
// 0 #  #
// 1 D  C
// 	3.	四面出击：寻找 index = 2（即字母 'C'）。
// •	往下走 (r+1)：到了 (1, 1) 字母是 'C'。进入下一层！
// 📍 第 3 层：位置 (1, 1)，字母 'C'，寻找 index = 2 ('C')
// 	1.	核对身份：当前是 'C'，我们要找的 word.charAt(2) 也是 'C'。匹配成功！
// 	2.	保存并隐身：
// •	temp = 'C'。
// •	board[1][1] = '#'。
// •	此时棋盘变成：
//   0  1
// 0 #  #
// 1 D  #
// 	3.	四面出击：寻找 index = 3（但我们单词长度只有 3）。
// •	随便往哪个方向走，传入的 index 都会变成 3。 进入下一层！
// 📍 第 4 层：Base Case 触发！
// 	1.	检查进度：传入的 index == 3，等于 word.length()。
// 	2.	宣布胜利：这说明所有的字母都找完了！直接 return true;。

// ⏪ 时光倒流（回溯的连锁反应）
// 因为 Java 中的 ||（逻辑或）有一个极其重要的特性叫短路机制：
// 只要其中一个条件是 true，后面的就不管了，直接向上汇报 true。
// 	1.	回到第 3 层 (字母 C)：
// •	因为往下走返回了 true，所以它剩下的 上、右、左 都不用试了。found = true。
// •	撤销选择：board[1][1] = temp（把 '#' 变回 'C'）。
// •	返回 true 给上一层。
// 	2.	回到第 2 层 (字母 B)：
// •	同样收到了 true。剩下的方向不用试了。
// •	撤销选择：board[0][1] = temp（把 '#' 变回 'B'）。
// •	返回 true 给上一层。
// 	3.	回到第 1 层 (字母 A)：
// •	收到了 true。
// •	撤销选择：board[0][0] = temp（把 '#' 变回 'A'）。
// •	返回 true 给主函数。
// 主函数收到 true，开心地结束循环，返回 true，打卡下班！
// 💡 核心总结
// 这里为什么要加个 #？
// 假设棋盘全是 'A'，我们要找 "AAA"。
// 如果不加 # 变成隐身人，走到第一个 'A' 时，它往右发现是 'A'，走到右边后，
// 往左一看“哎，又是一个 'A'”，就会在两个 'A' 之间疯狂反复横跳，形成死循环。
// # 的作用就是告诉自己人：“这块砖我刚踩过了，去找别的路！”


