// 🧐 疑问一：代码里真的没有核对当前字母吗？
// 其实是有的！它藏在 Base Case 2 那个很长的 if 判断语句的最后面。我们把那段代码单独拉出来看：
// // Base Case 2：撞墙了，或者找错人了
// if (r < 0 || c < 0 || r >= board.length || c >= board[0].length 
//     || board[r][c] != word.charAt(index)) { // <--- 【核心就在这半句！】
//     return false;
// }
// 注意看最后那个条件 board[r][c] != word.charAt(index)。
// 这句的意思正是：如果当前格子里的字母，和我们要找的 word 里的第 index 个字母不相等，立刻返回 false（宣告这条路死胡同）。
// 为什么只判断 index == word.length() 达到数量就行了？
// 因为代码是从上往下执行的。逻辑顺序是这样的：
// ---（不判断true，如果是对的字母就继续往下走，false的字母就返回，只判断走路长度即可。）---
// 	1.	先看 index 有没有达到单词长度。如果达到了，说明需要找的字母已经全部找完且匹配成功了，直接下班。
// 	2.	如果没找完，再去检查当前字母对不对（board[r][c] != word.charAt(index)）。
// 如果不对，直接拦截，根本不会有机会执行后面的 index + 1 进入下一层。
// 所以，只有当一个字母完全匹配时，它才有资格把 index + 1 传给下一层。
// 只要 index 能一路平安走到 word.length()，就绝对证明它前面经历的每一个字母都通过了质检！

// 🧱 疑问二：其他路径失败了是怎么算的？
// 为了看懂“失败是怎么处理的”，我们需要重点关注代码里的这句四面出击（逻辑或 || 连招）：
// boolean found = dfs(..., r + 1, c, index + 1) || // 往下走
//                 dfs(..., r - 1, c, index + 1) || // 往上走
//                 dfs(..., r, c + 1, index + 1) || // 往右走
//                 dfs(..., r, c - 1, index + 1);   // 往左走

// Java 里的 ||（或者）是这样工作的：从左到右挨个试。如果前面碰壁返回了 false，它就会接着试下一个方向。
// 如果四个方向都返回了 false，那么 found 就会变成 false。
// boolean found = false || false || false || false = false。
// 我们用一个“碰壁”的真实场景推演一下。
// 目标："ABC"
// 棋盘：
// A   B   X
// D   C   F
// 假设我们现在的路径是 A (0,0) -> B (0,1)。
// 现在我们要站在 B 的位置 (0, 1)，去四面出击寻找第三个字母 'C' (index=2)。此时，B 已经被标记为 # 隐身了。
// 	1.	往下走 (r+1)：走到 (1, 1)，格子里的字母是 'C'。
// •	board[1][1] == 'C'，匹配成功！它会继续往下深挖，最后触发 index == 3 返回 true。
// •	因为往下走通了，后面的（上、右、左）全都不需要再试了（这就是短路机制），直接向上一级汇报成功。
// 	2.	【假设我们把棋盘改一下，让往下走变成死路】
// 假设棋盘变成了这样，我们要找 "ABC"：
// A   B   X
// D  'Z'  F
// •	依然站在 B (0, 1)，找 'C'：
// •	往下试探：碰到 'Z'。'Z' != 'C'，碰壁！返回 false。
// •	往上试探：越界！返回 false。
// •	往右试探：碰到 'X'。'X' != 'C'，碰壁！返回 false。
// •	往左试探：碰到 '#'（刚才的 A 隐身了）。'#' != 'C'，碰壁！返回 false。
// 这个时候会发生什么？
// •	B 发起的四个方向全部惨遭 false 拒绝。
// •	所以 boolean found = false || false || false || false。
// •	结果 found 被赋值为 false。
// 接着，代码会执行到这里：
// // 4. 撤销选择（时光倒流）
// board[r][c] = temp; // 把 '#' 变回 'B'
// return found;       // 向给它布置任务的上一级（也就是 A）返回 false

// •	B 把自己恢复成原来的样子，然后对 A 说：“老大，我试了所有的方向，都不通，这条路废了 (false)。”
// •	收到 B 的失败报告后，A（位于 0,0）在 || 连招里发现“往右走（即 B）”行不通（是 false），A 就会叹口气，继续尝试它的下一个方向（比如往下走去试探 'D'）。
// 这就是回溯在面对失败路径时的精妙之处：试探 -> 碰壁 -> 恢复原样 -> 换个方向继续试。