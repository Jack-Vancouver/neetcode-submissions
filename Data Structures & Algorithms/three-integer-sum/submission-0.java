public class Solution {
    public List<List<Integer>> threeSum(int[] nums) {
        // 1. 终极前提：必须排序！
        // 排序后相同的数字会挨在一起，这是我们后续用双指针和“去重”的基础。
        Arrays.sort(nums); 
        
        // 准备一个“大抽屉”，用来装所有满足条件的“小抽屉”（三人组）
        List<List<Integer>> res = new ArrayList<>();

        // 2. 开始选老大 (固定指针 i，代表数字 A)
        // 为什么 i < nums.length - 2 也可以？因为留下最后两个位置给 L 和 R 即可。
        // 这里写 nums.length 也没错，内部 l < r 会拦住越界。
        for (int i = 0; i < nums.length; i++) {
            
            // 【神级优化 1】：如果排好序后，第一个数字（最小的数）都已经大于 0 了，
            // 后面全是正数，加起来绝对不可能等于 0，直接收工！
            if (nums[i] > 0) break;
            
            // 【核心去重 1】：老大的去重！
            // 如果当前的数字和上一个数字一模一样，说明这个“老大”我们刚才已经查过一遍了。
            // 为了防止出现重复的三人组，直接跳过它（continue）。
            if (i > 0 && nums[i] == nums[i - 1]) continue;

            // 3. 老大选定了，开始派左右两个小弟 (双指针) 找剩下的两个数
            int l = i + 1;              // 左小弟：从老大后一个位置开始
            int r = nums.length - 1;    // 右小弟：从数组最后面开始
            
            // 左右小弟开始向中间靠拢
            while (l < r) {
                // 计算当前三个人的总和
                int sum = nums[i] + nums[l] + nums[r];
                
                if (sum > 0) {
                    // 总和太大了，因为数组是排好序的，右边的数大，所以右小弟往左退一步
                    r--;
                } else if (sum < 0) {
                    // 总和太小了，左小弟往右进一步
                    l++;
                } else {
                    // 刚好等于 0！找到了！
                    // 把这三个数字打包成一个小抽屉 (List)，塞进大抽屉 (res) 里。
                    // Arrays.asList(...) 是一种快速创建一个小列表的便捷语法。
                    res.add(Arrays.asList(nums[i], nums[l], nums[r]));
                    
                    // 找到一组后，左右小弟都必须同时移动，去寻找下一组可能的组合
                    l++;
                    r--;
                    
                    // 【核心去重 2】：左小弟的去重！
                    // 此时左小弟已经走到新位置了。如果新位置的数字和刚才那个一模一样，
                    // 必须继续往右走，否则又会凑出完全一样的三人组！
                    // 注意必须保证 l < r，防止越界。
                    while (l < r && nums[l] == nums[l - 1]) {
                        l++;
                    }
                    // 注：为什么不写右小弟的去重？
                    // 因为左边去重了，总和必须是 0，右边的数字自然也会跟着避开重复组合。
                    // (当然你加上 r-- 的去重也可以，但不加也能跑通)。
                }
            }
        }
        return res;
    }
}